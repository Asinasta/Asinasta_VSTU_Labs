void task_1()
{
	// a. Объявление целой переменной, years;
	int years, ost;
	// b. Приглашение к вводу года;
	puts("Введите год, который вас интересует:");
	// c. Ввод с клавиатуры значений;
	scanf("%d", &years);
	// d. Вычисление остатка от деления введенного числа на 4;
	ost = years % 4;
	//  Применение структуры if–else для вывода на экран
	// одного из двух сообщений – «високосный год» и «год не високосный» 
	if (ost == 0) {
		printf("Год %d високосный\n", years);
	}
	else {
		printf("Год %d не високосный\n", years);
	}

}

