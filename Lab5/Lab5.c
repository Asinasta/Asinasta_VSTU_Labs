// Вариант 12. Дано трехзначное число, указать совпадающие разряды. 
// Программа должна выводить диагностическое сообщение вида «число единиц и десятков равно».

void task_3()
{
	int num, one, ten, hundred;

	puts("Введите ваше число:");
	scanf("%d", &num);

	// Извлекаем цифры у числа
	hundred = (num / 100);  // Деление на 100 дает сотни
	ten = ((num % 100) / 10);  // Остаток от деления на 100, затем делим на 10, получаем десятки
	one = (num % 10);  // Остаток от деления на 10, получаем единицы

	// Проверка введёных чисел
	// printf("Число - %d, Первая цифра - %d, Вторая цифра - %d, Третья цифра - %d\n", num, hundred, ten, one);

	// Поиск совпадающих разрядов и вывод диагностического сообщения
	if (one == ten) {
		printf("Число единиц и десятков равно\n");
	}
	if (ten == hundred) {
		printf("Число десятков и сотен равно\n");
	}
	if (hundred == one) {
		printf("Число сотен и единиц равно\n");
	}
	else {
		printf("Совпадающие разряды отсутствуют\n");
	}

}

int main()
{
	setlocale(LC_ALL, "RUS");
	task_3();
}